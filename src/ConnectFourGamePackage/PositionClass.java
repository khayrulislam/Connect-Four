package ConnectFourGamePackage;

public class PositionClass {

	
	private int xAxis,yAxis;

	public PositionClass(int xAxis, int yAxis) {
		super();
		this.xAxis = xAxis;
		this.yAxis = yAxis;
	}

	public int getxAxis() {
		return xAxis;
	}

	public int getyAxis() {
		return yAxis;
	}
	
	
}
